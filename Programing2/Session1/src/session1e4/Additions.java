package session1e4;

public class Additions {

    Additions (){
        //empty
    }

    int add(int n1, int n2){

        return n1+n2;
    }

    int add(int n1, int n2, int n3){
        return n1 + n2 + n3;
    }

    double add(double n1, double n2){
        return n1 + n2;
    }

    double add(double n1, double n2, double n3){
        return n1 + n2 + n3;
    }
}